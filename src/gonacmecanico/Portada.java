    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtUsuario.getText().equals("") || txtClave.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Rellena los campos por favor");
            txtUsuario.requestFocus();            
        }else{
        try {
            Statement st;
            st = cn.createStatement();
            String sql = "SELECT * FROM usuario WHERE usuario='" + txtUsuario.getText() + "'";
            ResultSet rs;
            rs = st.executeQuery(sql);
            while (rs.next()) {
                String clave = txtClave.getText();
                if (clave.equals(rs.getString("clave"))) {
                    JOptionPane.showMessageDialog(this, "Bienvenido " + rs.getString("nombre"));
                    categoria = rs.getString("categoria");
                    usuario = rs.getString("nombre");

                } 
            }
        } catch (SQLException ex) {
            Logger.getLogger(Portada.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (categoria.equals("categoria")){
           
                    JOptionPane.showMessageDialog(this, "Usuario no reconocido");
                    txtUsuario.setText("");
                    txtUsuario.requestFocus();
                    txtClave.setText("");
                
        }
        if (categoria.equals("Administrador")) {
            Principal frm = new Principal();
            frm.setVisible(true);
            this.dispose();
        }else if (categoria.equals("Operador")) {
            operador.Principal frm = new operador.Principal();
            frm.setVisible(true);
            operador.Principal.txtNombre.setText(usuario);
            this.dispose();
        }else if (categoria.equals("Mecanico")) {
            mecanico.Principal frm = new mecanico.Principal();
            frm.setVisible(true);
            mecanico.Principal.txtMecanico.setText(usuario);
            this.dispose();
        }
        }
            
        
    }//GEN-LAST:event_jButton1ActionPerformed

