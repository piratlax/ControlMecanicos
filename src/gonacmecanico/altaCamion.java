    void mostrarTabla() {
        String[] cabecera = {"Id", "Numero", "Tipo", "Marca", "Placa","Tipo/placa", "Serie","Modelo","Act."};
        String[] registros = new String[9];
        String sql = "SELECT * FROM vehiculo";
        //establecemos los anchos en pixeles de las columnas
        int[] anchos = {0, 70, 110, 100, 70, 80, 140,100,30};

        modelo = new DefaultTableModel(null, cabecera);
        try {

            Statement table;
            table = cn.createStatement();
            ResultSet rs = table.executeQuery(sql);
            while (rs.next()) {
                registros[0] = rs.getString("idUnidad");
                registros[1] = rs.getString("numero");
                registros[2] = rs.getString("tipo");
                registros[3] = rs.getString("marca");
                registros[4] = rs.getString("placa");
                registros[5] = rs.getString("tipo_placa");
                registros[6] = rs.getString("serie");
                registros[7] = rs.getString("modelo");
                registros[8] = rs.getString("activo");

                modelo.addRow(registros);

            }
            tabla.setModel(modelo);
            for (int i = 0; i < cabecera.length; i++) {
                tabla.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
                tabla.setFont(new java.awt.Font("Tahoma", 0, 12));
            }
            tabla.getColumnModel().getColumn(0).setMaxWidth(0);
        tabla.getColumnModel().getColumn(0).setMinWidth(0);
        tabla.getColumnModel().getColumn(0).setPreferredWidth(0);
        } catch (SQLException ex) {
            System.out.println("Sin poder ejecutar el query a la tabla");
        }

    }

