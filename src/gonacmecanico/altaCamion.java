    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        if (txtNumero.getText().equals("")){
            JOptionPane.showMessageDialog(this, "falta el numero de la Unidad");
            txtNumero.requestFocus();
        }else if (txtTipo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "falta el tipo de la unidad");
            txtTipo.requestFocus();
        }else if (txtMarca.getText().equals("")){
            JOptionPane.showMessageDialog(this, "falta la marca de la unidad");
            txtMarca.requestFocus();
        }else if (txtPlaca.getText().equals("")){
            JOptionPane.showMessageDialog(this, "falta la placa de la Unidad");
            txtPlaca.requestFocus();
        }else if (txtTipoPlaca.getText().equals("")){
            JOptionPane.showMessageDialog(this, "falta el tipo de placa de la Unidad");
            txtTipoPlaca.requestFocus();
        }else if (txtSerie.getText().equals("")){
            JOptionPane.showMessageDialog(this, "falta el numero de Serie");
            txtSerie.requestFocus();
        }else if (txtModelo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "falta el Modelo");
            txtModelo.requestFocus();
        }else if (txtMotor.getText().equals("")){
            JOptionPane.showMessageDialog(this, "falta el numero de Motor");
            txtMotor.requestFocus();
        }else if (txtPropiedad.getText().equals("")){
            JOptionPane.showMessageDialog(this, "falta el Propietario");
            txtPropiedad.requestFocus();
        }else {
        
        String numero=txtNumero.getText();
        String tipo=txtTipo.getText();
        String marca=txtMarca.getText();
        String placa=txtPlaca.getText();
        String tipoPlaca=txtTipoPlaca.getText();
        String serie=txtSerie.getText();
        String modelo=txtModelo.getText();
        String motor=txtMotor.getText();
        String propiedad=txtPropiedad.getText();
        String activo="si";
        int contador=0;
        
        try {
                // revisamos si no hay un nombre o un usario ya en la bd con los mismos datos
                String sql="SELECT * FROM vehiculo WHERE numero='"+numero+
                        "' or serie='"+serie+"' or motor='"+motor+"'";
                Statement st;
                st = cn.createStatement();
                ResultSet rs=st.executeQuery(sql);
               
                while (rs.next()){
                    JOptionPane.showMessageDialog(null,"El vehiculo esta repetido en la base de datos");
                    contador++;
                    
                }
                st.close();
                rs.close();
            } catch (SQLException ex) {
                Logger.getLogger(altaPersonal.class.getName()).log(Level.SEVERE, null, ex);
            }
        if (contador==0){
            try {
            //se prepara a insertarlo en la tabla de usuarios   
            Statement insertar=cn.createStatement();
            String sql="INSERT INTO vehiculo (numero,tipo,marca,placa,tipo_placa,serie,modelo,motor,propiedad) VALUES ('"+
                    numero+"','"+
                    tipo+"','"+
                    marca+"','"+
                    placa+"','"+
                    tipoPlaca+"','"+
                    serie+"','"+
                    modelo+"','"+
                    motor+"','"+
                    propiedad+"','"+
                    activo+"')";
                    
            insertar.executeUpdate(sql);
            insertar.close();
            
            
            
            JOptionPane.showMessageDialog(this,"se agregado el nuevo vehiculo");
            mostrarTabla();
            inicio();
            limpiar();
            
                       } catch (SQLException ex) {
            Logger.getLogger(altaPersonal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        }
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

