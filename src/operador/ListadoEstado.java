    private void buscarMecanico(){
        String buscar=txtMecanico.getText();
        try {
            Statement st=cn.createStatement();
            String sql="SELECT * FROM mecanico where completo='"+buscar+"'";
            ResultSet rs=st.executeQuery(sql);
            while (rs.next()){
                mecanico=rs.getString("id");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ListadoEstado.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
