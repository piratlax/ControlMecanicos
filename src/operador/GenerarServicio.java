    Date fechaMysql() {
        String fechaMysql = annio + "/" + mes + "/" + dia;
        SimpleDateFormat formato = new SimpleDateFormat("yyyy/MM/dd");
        try {
            Date parsed = formato.parse(fechaMysql);
            java.sql.Date sqlFecha = new java.sql.Date(parsed.getTime());
            return sqlFecha;
        } catch (ParseException ex) {
            Logger.getLogger(GenerarServicio.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }

    /**
     * @param args the command line arguments
     */
    private void colocarFecha() {
        Calendar c = Calendar.getInstance();
        dia = Integer.toString(c.get(Calendar.DATE));
        mes = Integer.toString(c.get(Calendar.MONTH));
        annio = Integer.toString(c.get(Calendar.YEAR));
        txtFecha.setText(dia + "-" + mes + "-" + annio);
        String hora = Integer.toString((c.get(Calendar.HOUR)));
        String min = Integer.toString((c.get(Calendar.MINUTE)));
        String mer = Integer.toString((c.get(Calendar.AM_PM)));
        String Meridiano = "AM";
        if (mer.equals("1")) {
            Meridiano = "PM";
        }
        txtHora.setText(hora + ":" + min + " " + Meridiano);
    }

