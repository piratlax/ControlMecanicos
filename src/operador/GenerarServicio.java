    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        txtVehiculo.setText(txtVehiculo.getText().toUpperCase());
        //buscamos si el auto no esta en servicio
        if (txtVehiculo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "te faltan campos por llenar");
            txtVehiculo.setText("");
            txtVehiculo.requestFocus();
        } else if (txtReporte.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "te faltan campos por llenar");
            txtReporte.setText("");
            txtReporte.requestFocus();
        } else {

            int contador = 0;
            String estado = "no";
            String marca = "marca";
            String placa = "placa";
            String modelo = "modelo";
            String tipo = "tipo";
            try {
                Statement st;
                st = cn.createStatement();
                String sql = "SELECT * FROM vehiculo WHERE numero='" + txtVehiculo.getText() + "'";
                ResultSet rs = st.executeQuery(sql);
                while (rs.next()) {
                    contador++;
                    estado = rs.getString("activo");
                    marca = rs.getString("marca");
                    placa = rs.getString("placa");
                    modelo = rs.getString("modelo");
                    tipo = rs.getString("tipo");

                }
            } catch (SQLException ex) {
                Logger.getLogger(GenerarServicio.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (contador == 0) {
                JOptionPane.showMessageDialog(this, "El vehiculo no se encuentra en el listado");
                txtVehiculo.setText("");
                txtVehiculo.requestFocus();
            } else if (estado.equals("no")) {
                JOptionPane.showMessageDialog(this, "El vehiculo se encuentra en servicio");
                txtVehiculo.setText("");
                txtVehiculo.requestFocus();
            } else {
                String mensaje = "Se va a generar el reporte\npara el vehiculo\nNumero " + txtVehiculo.getText()
                        + " Placa: " + placa + "\nTipo: " + tipo + " Modelo: " + modelo
                        + "\nMarca: " + marca;
                int respuesta = JOptionPane.showConfirmDialog(this, mensaje, "Confirmación", JOptionPane.YES_NO_OPTION);
                if (respuesta == JOptionPane.YES_OPTION) {
                    //procedemos a grabar el el servicio
                    String genero = txtPropietario.getText();
                    String vehiculo = txtVehiculo.getText();
                    String reporte = txtReporte.getText();
                    String hora = txtHora.getText();
                    java.sql.Date fecha = (java.sql.Date) fechaMysql();
                    String sql = "INSERT INTO servicio (genero,vehiculo,reporte,fecha,hora,estado) VALUES (?,?,?,?,?,?)";

                    try {
                        PreparedStatement pst = cn.prepareStatement(sql);
                        pst.setString(1, genero);
                        pst.setString(2, vehiculo);
                        pst.setString(3, reporte);
                        pst.setDate(4, fecha);
                        pst.setString(5, hora);
                        pst.setString(6, "enviado");
                        pst.executeUpdate();

                        //actualizamos vehiculo
                        Statement st;
                        st = cn.createStatement();
                        sql = "UPDATE vehiculo set activo='no' where numero='" + vehiculo + "'";
                        st.executeUpdate(sql);

                        JOptionPane.showMessageDialog(this, "El servicio esta\ncorrectamente generado");
                        operador.Principal frm = new operador.Principal();
                        frm.setVisible(true);
                        operador.Principal.txtNombre.setText(genero);
                        this.dispose();
                    } catch (SQLException ex) {
                        Logger.getLogger(GenerarServicio.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }
            }
        }
    }//GEN-LAST:event_btnGenerarActionPerformed

